void	ft_rev_int_tab(int *tab, int size)
{
	int	x;
	int	dif;

	x = 0;
	while (x < size - 1)
	{
		dif = tab[x];
		tab[x] = tab[size - 1];
		tab[size - 1] = dif;
		x++;
		size--;
	}
}
